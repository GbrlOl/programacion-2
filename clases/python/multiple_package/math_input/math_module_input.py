def suma_in():
    a = float(input("Ingresa el primer parámetro: "))
    b = float(input("Ingresa el segundo parámetro: "))

    return a + b


def resta_in():
    a = float(input("Ingresa el primer parámetro: "))
    b = float(input("Ingresa el segundo parámetro: "))
    return a - b


def multiplicacion_in():
    a = float(input("Ingresa el primer parámetro: "))
    b = float(input("Ingresa el segundo parámetro: "))
    return a * b


def division_in():
    a = float(input("Ingresa el primer parámetro: "))
    b = float(input("Ingresa el segundo parámetro: "))
    return a / b
